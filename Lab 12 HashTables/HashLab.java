class ProductCode {
    private String code;

    public ProductCode(String code) {
        this.code = code;
    }

    public int hashCode() {
        int value = 0;
		int constant = 40;

        // add up all the ASCII values
        for (int i = 0; i < code.length(); i++) {
			value = (constant * value) + code.charAt(i);
        }
        
        return value;
    }

    public String toString() {
        return code;
    } 
}

